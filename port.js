function start(x){
    x.style.color="aqua";
    x.style.border="2px solid pink";
    x.style.borderRadius="5px";
    x.style.borderWidth="thin";
    x.style.backgroundColor="pink";
}
function stop(x){
    x.style.color="white";
    x.style.border="None";
    x.borderRadius="100px"
    x.style.borderWidth="thick";
    x.style.backgroundColor="gray";
}